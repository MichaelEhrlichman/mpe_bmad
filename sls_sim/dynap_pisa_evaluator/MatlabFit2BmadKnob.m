
function bmadKnob = MatlabFit2BmadKnob(qfit,sc,order)
    if order==33
        bmadKnob=sprintf('a*%0.3f*%0.1e+b*%0.3f*%0.1e+a^2*%0.3f*%0.1e+a*b*%0.3f*%0.1e+ &\nb^2*%0.3f*%0.1e+a^3*%0.3f*%0.1e+a^2*b*%0.3f*%0.1e+a*b^2*%0.3f*%0.1e+b^3*%0.3f*%0.1e', ...
                  qfit.p10,sc, ...
                  qfit.p01,sc, ...
                  qfit.p20,sc^2, ...
                  qfit.p11,sc^2, ...
                  qfit.p02,sc^2, ...
                  qfit.p30,sc^3, ...
                  qfit.p21,sc^3, ...
                  qfit.p12,sc^3, ...
                  qfit.p03,sc^3);
    elseif order==3
        bmadKnob=sprintf('a*%0.3f*%0.1e+a^2*%0.3f*%0.1e+a^3*%0.3f*%0.1e', ...
                  qfit.p3,sc, ...
                  qfit.p2,sc^2, ...
                  qfit.p1,sc^3);
    elseif order==2              
        bmadKnob=sprintf('a*%0.3f*%0.1e+a^2*%0.3f*%0.1e', ...
                  qfit.p2,sc, ...
                  qfit.p1,sc^2);
    end
end
